import static java.lang.Math.*;
public class math {

    public static void main(String[] args) {
        double a = 10.10;
        double b = -5.0;
        double c = 0.5; 
        System.out.println("Absolute value of " + b + " is: " + abs(b));
        System.out.println("Ceiling of " + c + " is: " + ceil(c));
        System.out.println("Floor of " + c + " is: " + floor(c));   
        System.out.println("Maximum of " + a + " and " + b + " is: " + max(a, b));
        System.out.println("Minimum of " + a + " and " + b + " is: " + min(a, b));
        System.out.println("Square root of " + a + " is: " + sqrt(a));
        System.out.println("Power of " + a + " raised to 2 is: " + pow(a, 2));
        
        
    }
}
