class swichj{
	public static void main(String[] args){
		String day = "Wed";
		switch(day){
			case "Mon":
				System.out.println("Monday");
				break;
			case "Tue":
				System.out.println("Tuesday");
				break;
			case "Wed":
				System.out.println("Wensday");
				break;
			case "Thu":
				System.out.println("Thusday");
				break;
		}
	}
}